import type { LocationOption } from "../../types/location";

function majorLocation(
  id: string,
  cityName: string,
  countryCode: string,
  latitude: number,
  longitude: number,
  cityCode: string | null = null,
  state?: string
): LocationOption {
  return {
    id,
    cityName,
    cityCode,
    countryCode,
    state,
    latitude,
    longitude,
    label: [cityName, state, countryCode].filter(Boolean).join(", ")
  };
}

